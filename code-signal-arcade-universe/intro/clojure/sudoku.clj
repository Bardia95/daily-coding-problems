(defn r [grid i]
  (not= (range 1 10) (sort (grid i))))

(defn c [grid i]
  (not= (range 1 10)
        (sort (for [x (range 9)]
                ((grid x) i)))))

(defn g [grid x y]
  (not= (range 1 10)
        (sort (for [i (range x (+ x 3))
                    j (range y (+ y 3))]
                ((grid i) j)))))


(defn sudoku [grid]
  (loop [i 0]
    (if (= i 9)
      (if (some true? (vec (for [i (range 0 9 3)
                                 j (range 0 9 3)]
                             (g grid i j))))
        false
        true)
      (if (or (r grid i) (c grid i))
        false
        (recur (inc i))))))


